    private void txtMeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMeKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            evt.setKeyCode(0);
            if (onlineList.getSelectedIndex() == -1 && !sendToAllCheckbox.isSelected())
                return;
            String to_who = (String)onlineList.getSelectedValue();
            mc.send(to_who, txtMe.getText(), sendToAllCheckbox.isSelected());
            if (sendToAllCheckbox.isSelected() == false 
                    && (to_who == null || !to_who.equals(txtNick.getText()))) {
                drawMessage(txtNick.getText(), txtMe.getText(),
                        System.currentTimeMillis());
            }
            txtMe.setText("");
        }
}//GEN-LAST:event_txtMeKeyPressed

