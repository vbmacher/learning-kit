    private class WeirdListModel extends AbstractListModel {
        private int size = 0;
        public void add() {
            size = mc.getOnlineList().length;
            fireContentsChanged(this, size, size);            
        }
        public void remove() {
            size = mc.getOnlineList().length;
            fireIntervalRemoved(this, size, size);
        }
        public int getSize() {
            return mc.getOnlineList().length;
        }
        public Object getElementAt(int index) {
            return mc.getOnlineList()[index];
        }
    }
    
    /**
     * draws message to glass..
     */
    public void drawMessage(String from, String data, long time) {
        try {
            txtHim.getStyledDocument().insertString(0, data + "\n", plainFont);
            txtHim.getStyledDocument().insertString(0, 
                    from + "(" + sdf.format(new Date(time)) + "):\n", boldFont);
        } catch (BadLocationException ex) {}
    }
    
