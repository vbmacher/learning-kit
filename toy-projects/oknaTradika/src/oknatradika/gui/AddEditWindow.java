    public void enableItems() {
        if (chkSpojovaciProfil.isSelected()) {
            chkOwnLength.setEnabled(true);
            chkSpojDPH.setEnabled(true);
            if (chkOwnLength.isSelected())
                txtSpojLength.setEnabled(true);
            else
                txtSpojLength.setEnabled(false);
        } else {
            chkOwnLength.setEnabled(false);
            txtSpojLength.setEnabled(false);
            chkSpojDPH.setEnabled(false);
        }
    }
    
    public AddEditWindow(java.awt.Frame parent, boolean modal, Input win) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(parent);
        this.loadTypes();
        this.setTitle("Upraviť okno");
        this.txtHeight.setText(String.valueOf(win.getVyska()));
        this.txtWidth.setText(String.valueOf(win.getSirka()));
        chkVonkajsiaP.setSelected(win.getVON());
        chkVnutornaP.setSelected(win.getVNU());
        chkVonDPH.setSelected(win.getDphVON());
        chkVnuDPH.setSelected(win.getDphVNU());
        chkMontaz.setSelected(win.getMON());
        chkZaluzie.setSelected(win.getZAL());
        chkVyspravky.setSelected(win.getVYS());
        chkSietky.setSelected(win.getSIE());
        chkZalDPH.setSelected(win.getDphZAL());
        chkMonDPH.setSelected(win.getDphMON());
        chkVysDPH.setSelected(win.getDphVYS());
        chkSieDPH.setSelected(win.getDphSIE());
        chkOknoDPH.setSelected(win.getDphOKN());
        txtZlava.setText(String.valueOf(win.getZlava()));
        txtKoeficient.setText(String.valueOf(win.getKoeficient()));

        chkSpojovaciProfil.setSelected(win.getSPOJ());
        chkSpojDPH.setSelected(win.getDphSPOJ());
        chkOwnLength.setSelected(win.getOwnLength());
        if (chkOwnLength.isSelected())
            txtSpojLength.setText(String.valueOf(win.getSpojLength()));
        enableItems();
        
        type_model.selectAtId(win.getTyp().getID());
        WindowSubtype[] wst = WindowSubtype.getAllSubtypes(win.getTyp().getID());
        subtype_model = new WSubtypeComboModel(wst);
        cmbPodtyp.setModel(subtype_model);
        subtype_model.selectAtId(win.getPodtyp().getID());

        WindowSubtype w = win.getPodtyp();
        if (w.getPicture() != null)
            lblPicture.setIcon(w.getPicture());
        else
            lblPicture.setIcon(null);

        cmbTyp.revalidate();
        cmbTyp.repaint();
        cmbPodtyp.revalidate();
        cmbPodtyp.repaint();
        spnCount.getModel().setValue(win.getKusov());
    }
    

