private void btnVypocetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVypocetActionPerformed
    int width = 0;
    int height = 0;
    int zlava = 0;
    int kusov = 0;
    float koef = 0;
    int spojLength = 0;
    try { width = Integer.parseInt(txtWidth.getText()); }
    catch(Exception e) {
        Main.error("Neplatná šírka okna. Použite celé čísla");
        txtWidth.grabFocus();
        return;
    }
    try { height = Integer.parseInt(txtHeight.getText()); }
    catch(Exception e) {
        Main.error("Neplatná výška okna. Použite celé čísla");
        txtHeight.grabFocus();
        return;
    }
    try { zlava = Integer.parseInt(txtZlava.getText()); }
    catch(Exception e) {
        Main.error("Neplatná zľava. Použite celé čísla");
        txtZlava.grabFocus();
        return;
    }
    try { koef = Float.parseFloat(txtKoeficient.getText()); }
    catch (Exception e) {
        Main.error("Neplatný koeficient ceny okna. Použite desatinné čísla");
        txtKoeficient.grabFocus();
        return;
    }
    try { kusov = Integer.parseInt(spnCount.getModel().getValue().toString()); }
    catch(Exception e) {
        Main.error("Neplatný počet kusov okna. Použite celé čísla");
        spnCount.grabFocus();
        return;        
    }
    try { spojLength = Integer.parseInt(txtSpojLength.getText()); }
    catch(Exception e) {
        Main.error("Neplatná dĺžka spojovacieho profilu. Použite celé čísla");
        txtSpojLength.grabFocus();
        return;        
    }
        
    window = new Input(width, height, zlava, koef, chkVonkajsiaP.isSelected(),
            chkVnutornaP.isSelected(), chkZaluzie.isSelected(), 
            chkMontaz.isSelected(), chkVyspravky.isSelected(), 
            chkSietky.isSelected(), chkVonDPH.isSelected(),
            chkVnuDPH.isSelected(), chkZalDPH.isSelected(),
            chkMonDPH.isSelected(), chkVysDPH.isSelected(),
            chkSieDPH.isSelected(), chkOknoDPH.isSelected(), 
            new WindowType(type_model.getIdAt(cmbTyp.getSelectedIndex())),
            new WindowSubtype(subtype_model.getIdAt(cmbPodtyp.getSelectedIndex())),
            kusov, chkSpojovaciProfil.isSelected(),
            chkOwnLength.isSelected() ? spojLength : height, 
            chkSpojDPH.isSelected(), chkOwnLength.isSelected());
    OOK = true;
    dispose();
}//GEN-LAST:event_btnVypocetActionPerformed

private void cmbTypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTypActionPerformed
    if (cmbTyp.getSelectedIndex() != -1) {
        try {
            WindowSubtype[] wst = WindowSubtype.getAllSubtypes(
                    ((WTypeComboModel)cmbTyp.getModel())
                    .getIdAt(cmbTyp.getSelectedIndex()));
            subtype_model = new WSubtypeComboModel(wst);
            cmbPodtyp.setModel(subtype_model);
            cmbPodtyp.revalidate();
            cmbPodtyp.repaint();
        } catch (Exception e) {}
    }
}//GEN-LAST:event_cmbTypActionPerformed

private void cmbPodtypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPodtypActionPerformed
    if (cmbPodtyp.getSelectedIndex() != -1) {
        WindowSubtype w = subtype_model.getAt(cmbPodtyp.getSelectedIndex());
        if (w.getPicture() != null)
            lblPicture.setIcon(w.getPicture());
        else
            lblPicture.setIcon(null);
    }
}//GEN-LAST:event_cmbPodtypActionPerformed

