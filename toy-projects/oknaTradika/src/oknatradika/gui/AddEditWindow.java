    private void loadTypes() {
        WindowType[] wt = WindowType.getAllTypes();
        this.type_model = new WTypeComboModel(wt);
        this.subtype_model = new WSubtypeComboModel(null);

        this.cmbTyp.setModel(type_model);
        this.cmbPodtyp.setModel(subtype_model);
        cmbTyp.revalidate();
        cmbTyp.repaint();
        cmbPodtyp.revalidate();
        cmbPodtyp.repaint();
    }
    
