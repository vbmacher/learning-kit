    private void insertHTML(String html) {
        HTMLEditorKit kit = (HTMLEditorKit)txtResults.getEditorKit();
        Document doc = txtResults.getDocument();
        StringReader reader = new StringReader(html);
        try { kit.read(reader, doc, 0); } catch (Exception e) {}
    }
    
