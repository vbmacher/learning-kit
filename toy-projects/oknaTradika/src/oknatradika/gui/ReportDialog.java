private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    JFileChooser f = new JFileChooser();
    NiceFileFilter f1 = new NiceFileFilter();
    NiceFileFilter f2 = new NiceFileFilter();
    NiceFileFilter f3 = new NiceFileFilter();
    NiceFileFilter f4 = new NiceFileFilter();

    f1.addExtension("rtf");
    f1.setDescription("Dokument RichText (*.rtf)");
    f2.addExtension("html");
    f2.setDescription("HTML stránka (*.html)");
    f3.addExtension("txt");
    f3.setDescription("Textový súbor (*.txt)");
    f4.addExtension("*");
    f4.setDescription("Všetky súbory (*.*)");

    f.setDialogTitle("Uložiť súbor");
    f.setAcceptAllFileFilterUsed(false);
    f.addChoosableFileFilter(f1);
    f.addChoosableFileFilter(f2);
    f.addChoosableFileFilter(f3);
    f.addChoosableFileFilter(f4);
    f.setFileFilter(f1);
    f.setApproveButtonText("Uložiť");

    int returnVal = f.showSaveDialog(this);
    f.setVisible(true);
    if (returnVal != JFileChooser.APPROVE_OPTION) return;
    
    File fileSource = f.getSelectedFile();
    if (!(fileSource.canWrite() == true || fileSource.exists() == false )) {
        Main.error("Nesprávny názov súboru");
        return;
    }
    String fn = fileSource.getAbsolutePath();
    NiceFileFilter fil = (NiceFileFilter)f.getFileFilter();
    if (fil.getExtension(fileSource) == null && fil.getFirstExtension()!=null) {
        if (!fil.getFirstExtension().equals("*"))
            fn +="."+fil.getFirstExtension();
    }
    fileSource = new java.io.File(fn);
    new WorkingDialog(this,true,fileSource,res,txtResults).setVisible(true);
}//GEN-LAST:event_btnSaveActionPerformed

   

