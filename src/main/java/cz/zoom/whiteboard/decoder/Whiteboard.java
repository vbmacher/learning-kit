    private class SnapshotListener implements WebcamListener {

        public void webcamOpen(WebcamEvent we) {
            canvas.setWebcam(we.getSource(), new WebcamMotionListener() {
                public void motionDetected(WebcamMotionEvent wme) {
                    scanTasks();
                }
            });
        }

        private void scanTasks() {
            tasks.clear();
            try {
                Task[] tmpTasks = TaskTransformations.decodeAll(canvas.snapshot());
                if (tmpTasks == null) {
                    return;
                }
                tasks.addAll(Arrays.asList(tmpTasks));
                tasksModel.fireTableDataChanged();
            } catch (NotFoundException e) {
                // QR code was not found... ignore
            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
            }
        }

        public void webcamClosed(WebcamEvent we) {
            canvas.setWebcam(null, null);
        }

        public void webcamDisposed(WebcamEvent we) {
        }
    }

